import turtle
turtle.shape('turtle')
turtle.speed(1000000000000000000000000000000000000)
turtle.begin_fill()
turtle.color("yellow")
for i in range(360):		
	turtle.left(1)
	turtle.forward(2)
turtle.end_fill()
turtle.goto(50, 120)
turtle.begin_fill()
turtle.color("blue")
for i in range(360):		
	turtle.left(1)
	turtle.forward(1/5)
turtle.end_fill()
turtle.penup()
turtle.goto(-50, 120)
turtle.pendown()
turtle.begin_fill()
turtle.color("blue")
for i in range(360):		
	turtle.left(1)
	turtle.forward(1/5)
turtle.end_fill()
turtle.penup()
turtle.goto(-45, 50)
turtle.pendown()
turtle.width(8)
turtle.color('red')
turtle.left(90)
for i in range(180):		
	turtle.right(1)
	turtle.forward(0.7)
turtle.penup()
turtle.goto(0,105)
turtle.right(180)
turtle.pendown()
turtle.width(8)
turtle.color('black')
